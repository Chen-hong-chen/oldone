/**
  * @brief This function handles UART4 global interrupt.
  */
void UART4_IRQHandler(void)
{
  /* USER CODE BEGIN UART4_IRQn 0 */
    uint32_t length;

        if((__HAL_UART_GET_FLAG(&huart4,UART_FLAG_IDLE) != (int)RESET))
        {
            if(receivebuff_4G.receive_flag == 0u)
            {
              receivebuff_4G.rx_len = 0u;
              HAL_TIM_Base_Start_IT(&htim7);
            }
            __HAL_TIM_SET_COUNTER(&htim7,0u);
            __HAL_UART_CLEAR_IDLEFLAG(&huart4);
            __HAL_UART_FLUSH_DRREGISTER(&huart4);
            HAL_UART_DMAStop(&huart4);
            length = huart4.hdmarx->Instance->CNDTR;
            length = (uint32_t)EC80_RECEIVE_LEN - length;
            memcpy(&receivebuff_4G.usartDMA_rxBuf[receivebuff_4G.rx_len],rx_buffer,length);
            receivebuff_4G.rx_len += (uint8_t)length;
            receivebuff_4G.receive_flag = 1u;

            HAL_UART_Receive_DMA(&huart4,(uint8_t * )rx_buffer,(uint16_t)EC80_RECEIVE_LEN);
        }

  /* USER CODE END UART4_IRQn 0 */
  HAL_UART_IRQHandler(&huart4);
  /* USER CODE BEGIN UART4_IRQn 1 */

  /* USER CODE END UART4_IRQn 1 */
}

